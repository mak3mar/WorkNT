# 1. Сформировать круговой массив от 1 до n
# 2. Сформировать массив (список) для "пути"
# 3. Обойти круговой массив
# 4. Сформировать строку и вывести

# Функция обхода кругового массива
def circular_array_path(n, m):
    # Круговой массив, который представляет собой список чисел от 1 до n
    circular_array = [i for i in range(1, n+1)]
    
    # Начальная позиция в массиве
    current_position = 0

    # Массив для хранения пути
    path = []
    
    # Пока не вернемся к первому элементу
    while True:
        # Если достигли первого элемента, прекращаем
        if current_position == 0 and len(path) > 1:
            break

        # Добавляем текущий элемент в путь
        path.append(circular_array[current_position])
        
        # Переходим на m-1 позиций вперед (так как первая позиция у нас уже учтена)
        current_position = (current_position + m - 1) % n

    # Объединяем элементы массива в единую строку 
    return "".join(map(str, path)) # (map превращает каждый элемент в str, join "склеивает" в одну строку с разделителем - пустой строкой)

# Основное тело программы
if __name__ == "__main__":
    # Проверяем, что передано два аргумента
    if len(sys.argv) != 3: # (первый аргумент - имя скрипта)
        print("Введите два целых числа через пробел: n m")
        sys.exit(1)
    
    # Пытаемся преобразовать аргументы в целые числа
    try:
        n = int(sys.argv[1])
        m = int(sys.argv[2])
    except ValueError:
        print("Оба аргумента должны быть целыми числами!")
        sys.exit(1)
    
    # Выводим результат
    print(circular_array_path(n, m))
